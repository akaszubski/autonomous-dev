#!/usr/bin/env python3
"""Measure IntentClassifier accuracy against the real corpus.

Runs the real IntentClassifier from lib/intent_classifier.py against each
entry in the corpus file, computes per-class precision/recall/F1, builds
a confusion matrix, and identifies the bottom-3 underperforming classes.

Optionally writes results to docs/intent_classifier_calibration.json and
splices a Markdown report into docs/INTENT-CLASSIFICATION.md (idempotent).

Usage:
    python scripts/measure_intent_classifier.py \\
        --corpus-path tests/fixtures/intent_classifier_real_corpus.json \\
        --baseline-output docs/intent_classifier_calibration.json \\
        --update-docs

GitHub Issue: #1043
"""

